                <p class="card-body" style="text-align:justify">
                    The Magna Carta of Women (Republic Act No. 9710) defines Gender and Development (GAD) as the development perspective and process that is participatory and empowering, equitable, sustainable, free from violence, respectful of human rights, supportive of self-determination and actualization of human potentials. It seeks to achieve gender equality as a fundamental value that should be reflected in development choices and contends that women are active agents of development, not just passive recipients of development.

                    <br><br>
                    GAD focuses on Gender Mainstreaming or a strategy for:

                    <br><br>

                    <b>–</b> Making women’s as well as men’s concerns and experiences an integral dimension of the design, implementation, monitoring, and evaluation of policies, programs and projects in all social, political, civil, and economic spheres so that women and men benefit equally; and

                    <br><br>

                    <b>–</b> Assessing the implications for women and men of any planned action, including legislation, policies or programs in all areas and at all levels.

                    <br><br>
                    The implementation of GAD in government agencies, including GOCCs, finds basis in various laws and Presidential issuances.

                    <br><br>

                    <b>GENDER AND DEVELOPMENT IN THE PHILIPPINES</b>

                    <br><br>
                    <b>(1) Executive Order No. 273 – Approving and Adopting the Philippine Plan for Gender-Responsive Development (PPGD) 1995-2025</b>

                    <br><br>

                    <b>Executive Order (EO) 273</b>, issued on September 9, 1995 and signed by President Fidel V. Ramos, adopted the Philippine Plan for Gender Responsive Development IPPGD) 1995-2025. The PPGD 1995-2025 is a 30 year perspective plan that outlines the policies, strategies, programs and projects that the government must adopt to enable women to participate in and benefit from national development while EO 273 directs all government agencies, departments, bureaus, offices and instrumentalities, including government owned and controlled corporations, at the national level, sub-national and local levels to:

                    <br><br>

                    1.1. To take appropriate steps to ensure the full implementation of the policies/strategies and programs/projects outlined in the Plan;

                    <br><br>

                    1.2. To institutionalize Gender and Development (GAD) efforts in government by incorporating GAD concerns, as spelled out in the Plan, in their planning, programming and budgeting processes, but specifically to:

                    <br><br>

                    1.2.1. Include/incorporate GAD concerns in the :

                    <br>
                    a. formulation, assessment and updating of their annual agency plans;
                    <br>
                    b. formulation, assessment and updating of their inputs to the medium/long-term development plans, and
                    <br>
                    c. preparation of their inputs to sectoral performance assessment reports, public investment plans and other similar documents.
                    <br><br>
                    1.2.2. Incorporate and reflect GAD concerns in their:
                    <br>
                    a. agency performance commitments contracts indicating key result areas for GAD as well as in  their annual performance report to the President, and
                    <br>
                    b. annual Agency Budget Proposals and work and financial plans

                    <br><br>
                    <b>(2) Republic Act No. 9710 – An Act Providing for the Magna Carta of Women</b>

                    <br><br>
                    <b>
                        Section 36: Gender Mainstreaming as a Strategy for Implementing the Magna Carta of Women</b>
                        <br>
                        <i>“All government departments, including their attached agencies, offices, bureaus, state universities and colleges, government owned and controlled corporations, local government units and all other government instrumentalities shall adopt gender mainstreaming as a strategy to promote women’s human rights and eliminate gender discrimination in their systems, structures, policies, programs, processes and procedures.
                        <br><br> </i>
                        <b>
                        Section 36-B: Creation and/or Strengthening of the GAD Focal Points (GFP)</b><br><i>
                        “All concerned government agencies and instrumentalities mentioned above shall establish or strengthen their GAD Focal Point System (GFPS) or a similar GAD mechanism to catalyze and accelerate gender mainstreaming within the agency.
                        In addition to Joint Circular 99-4 issued by the National Economic and Development Authority (NEDA), the DBM and NCRFW, additional sets of guidelines, as deemed necessary, shall be formulated, for the implementation of GAD-related programs/projects/activities.
                        </i>

                        <br><br>
                        <b>(3) RA No. 11936 (“Act Providing for the Operation of the Government of the Republic of the Philippines from January 1 to December 31, 2023”)</b>

                        <br><br>
                        <b>Section 35.</b> Programs and Projects Related to Gender and Development. All agencies of the government shall formulate a Gender and Development (GAD) Plan designed to address gender issues within their concerned sectors or mandate and implement the applicable provisions under RA No. 9710 or the Magna Carta of Women, Convention on the Elimination of All Forms of Discrimination Against Women, the Beijing Platform for Action, and the Philippine Plan for Gender-Responsive Development (1995-2025) and the Philippine Development Plan.
                        <br><br>
                        The GAD Plan shall be integrated in the regular activities of the agencies, which shall be at least five percent (5%) of their budgets. For this purpose, activities currently being undertaken by the agencies which related to GAD or those that contribute to poverty alleviation, economic empowerment especially if marginalized women, protection, promotion, and fulfillment of women’s human rights, and practice of gender-responsive governance are considered sufficient compliance with said requirement. Utilization of GAD budget shall be evaluated based on the GAD performance indicators identified by said agencies.



